#include <iostream>      // Для работы с вводом/выводом (cout, cin, endl)
#include <vector>        // Для использования динамического массива vector
using namespace std;     // Стандартное пространство имён (чтобы писать cout, vector без std::)

// Сортировка пузырьком (BUBBLE SORT)
void bubbleSort(vector<int>& arr) {               // Функция принимает массив по ссылке, чтобы менять его прямо
    int n = arr.size();                           // Определяем размер массива
    for (int i = 0; i < n - 1; i++) {             // Внешний цикл: количество проходов по массиву
        for (int j = 0; j < n - i - 1; j++) {     // Внутренний цикл: сравниваем соседние элементы
            if (arr[j] > arr[j + 1]) {            // Если текущий элемент больше следующего
                swap(arr[j], arr[j + 1]);         // Меняем их местами с помощью встроенной функции swap
            }
        }
    }
}


// Сортировка выбором (SELECTION SORT)
void selectionSort(vector<int>& arr) {            // Функция принимает массив по ссылке, чтобы менять его прямо
    int n = arr.size();                           // Определяем размер массива
    for (int i = 0; i < n - 1; i++) {             // Внешний цикл: проходим по массиву
        int minIndex = i;                         // Инициализируем индекс минимального элемента текущего прохода
        for (int j = i + 1; j < n; j++) {         // Внутренний цикл: ищем минимальный элемент в оставшейся части массива
            if (arr[j] < arr[minIndex]) {         // Если нашли элемент меньше текущего минимума
                minIndex = j;                     // Обновляем индекс минимального элемента
            }
        }
        swap(arr[i], arr[minIndex]);              // Меняем текущий элемент с найденным минимальным
    }
}

// Сортировка вставкой (INSERTION SORT)
void insertionSort(vector<int>& arr) {            // Функция принимает массив по ссылке, чтобы менять его прямо
    int n = arr.size();                           // Определяем размер массива
    for (int i = 1; i < n; i++) {                 // Проходим по элементам массива начиная со второго
        int key = arr[i];                         // Сохраняем текущий элемент в переменную key
        int j = i - 1;                            // Начинаем проверку отсортированной части слева
        while (j >= 0 && arr[j] > key) {          // Пока не дошли до начала и элементы больше key
            arr[j + 1] = arr[j];                  // Сдвигаем элемент вправо
            j--;                                  // Переходим к предыдущему элементу
        }
        arr[j + 1] = key;                         // Вставляем key на правильное место
    }
}


// Функция для вывода массива
void printArray(const vector<int>& arr) {         // Функция принимает массив по константной ссылке (не изменяем)
    for (int val : arr) {                         // Для каждого элемента массива
        cout << val << " ";                       // Выводим его в консоль через пробел
    }
    cout << endl;                                 // Переходим на новую строку после вывода всего массива
}



int main() {                            // Основная функция
    vector<int> data = {5, 2, 19, 0, 5, 6};       // Создаём исходный массив для сортировки

    cout << "Исходный массив: ";                  // Выводим исходный массив
    printArray(data);                             // Вызываем функцию вывода

    // Bubble sort
    vector<int> bubbleArr = data;                 // Создаём копию массива для сортировки пузырьком
    bubbleSort(bubbleArr);                        // Вызываем функцию пузырьковой сортировки
    cout << "После сортировки пузырьком: ";       // Выводим результат
    printArray(bubbleArr);                        // Выводим отсортированный массив

    // Selection sort
    vector<int> selectionArr = data;              // Создаём копию массива для сортировки выбором
    selectionSort(selectionArr);                  // Вызываем функцию сортировки выбором
    cout << "После сортировки выбором: ";         // Выводим результат
    printArray(selectionArr);                     // Выводим отсортированный массив

    // Insertion sort
    vector<int> insertionArr = data;              // Создаём копию массива для сортировки вставкой
    insertionSort(insertionArr);                  // Вызываем функцию сортировки вставкой
    cout << "После сортировки вставкой: ";        // Выводим результат
    printArray(insertionArr);                     // Выводим отсортированный массив

    return 0;                                     // Завершаем программу
}
